<h3>Tier List Disclaimer</h3>

    <p>
     We chose not to include the Tier 5 Neutral items for a multitude of reasons. Primarily, since they are widely
     considered to objectively be the best neutral items, their value would inflate the higher levels of our tier list.
     Furthermore, the majority of our games end before these items have time to drop. Because of this, we are personally inexperienced
     with these tier 5 items, therefore we decided we didn't have enough knowledge to accurately place them in the
    different tiers of the list.
    </p>

